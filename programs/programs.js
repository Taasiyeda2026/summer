const grid = document.getElementById('programGrid');
const overlay = document.getElementById('overlay');
const closeBtn = document.getElementById('closeBtn');
const modalBadge = document.getElementById('modalBadge');
const modalTitle = document.getElementById('modalTitle');
const modalSubtitle = document.getElementById('modalSubtitle');
const modalMeta = document.getElementById('modalMeta');
const modalPrice = document.getElementById('modalPrice');
const modalPdf = document.getElementById('modalPdf');
const modalSummary = document.getElementById('modalSummary');

programs.forEach((program, index) => {
  const card = document.createElement('button');
  card.type = 'button';
  card.className = 'program-card';
  card.setAttribute('aria-haspopup', 'dialog');
  card.setAttribute('aria-label', `פתיחת ${program.shortName}`);
  card.innerHTML = `
    <div class="program-name">${program.shortName}</div>
    <div class="card-footer">
      <span class="badge ${program.badgeClass}">${program.badge}</span>
      <span class="plus">+</span>
    </div>
  `;
  card.addEventListener('click', () => openModal(index));
  grid.appendChild(card);
});

function openModal(index) {
  const program = programs[index];
  modalBadge.className = `badge ${program.badgeClass}`;
  modalBadge.textContent = program.badge;
  modalTitle.textContent = program.title;
  modalSubtitle.textContent = program.subtitle;
  modalMeta.textContent = program.meta;
  modalPrice.textContent = program.price;
  modalPdf.href = program.pdf;
  modalPdf.setAttribute('aria-label', `פתיחת פרטי התוכנית והסילבוס המלאים של ${program.shortName}`);
  modalSummary.innerHTML = program.paragraphs.map((paragraph) => `<p>${paragraph}</p>`).join('');
  overlay.classList.add('open');
  overlay.setAttribute('aria-hidden', 'false');
  document.body.classList.add('modal-open');
}

function closeModal() {
  overlay.classList.remove('open');
  overlay.setAttribute('aria-hidden', 'true');
  document.body.classList.remove('modal-open');
}

closeBtn.addEventListener('click', closeModal);
overlay.addEventListener('click', (event) => {
  if (event.target === overlay) closeModal();
});
document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape' && overlay.classList.contains('open')) closeModal();
});